# INTEGERS (represent numbers)
puts 5 + 7

puts 12/3

# STRINGS (represent characters)
"hello"

# string concatenation (combining strings)
puts "hello" + " " + "world"

# string interpolation
puts "hello #{5 + 7}"

# built in methods (see ruby docs for more)
puts "hello world".upcase

# variables (store data for later use and manipulation)

first_name = "Kiki"

puts "hello #{first_name.upcase}"

puts first_name

age = 21
puts age
# one year passes
# incrementing (by 1) (manipulating and reusing data)
age = age + 1
puts age
